/*
	Buffers
	Buffers are the memory areas of WebGL that hold the data.
	
	There are various buffers namely,
		drawing buffer,
		frame buffer,
		vetex buffer, and
		index buffer.
		
	The vertex buffer and index buffer are used to describe and process the geometry of the model.
	
	Vertex buffer objects:
		This stores data corresponding to each vertex (per-vertex data)
	Index buffer objects:
		This stores data about the indices.
	Frame buffer
		is a portion of graphics memory that hold the scene data.
		This buffer contains details such as width and height of the surface (in pixels), color of each pixel and their depth.
*/