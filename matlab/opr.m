% arithmetic
+       % addition    or unary plus
-       % subtraction or unary minus
.*      % element-wise multiplication
*       % matrix       multiplication
./      % element-wise right division
/       % matrix       right division
.\      % element-wise left division 
\       % matrix       left division
.^      % element-wise power
^       % matrix       power
.'      % transpose
'       % complex conjugate transpose'

% relational
==      % equal to
~=      % not equal to
>       % greater than
>=      % greater than or equal to
<       % less than
<=      % less than or equal to

% logical
&       % and
|       % or 
&&      % and (with short-circuiting)
||      % or  (with short-circuiting)
~       % not

% special chars
@       % function handle construction and reference,		calling superclass methods
.       % decimal point, 																element-wise operations,										structure field access,								object property or method specifier
...     % line continuation
,       % separator
:       % vector creation, 															indexing, 																	for-loop iteration
;       % signify end of row, 													suppress output of code line
()      % operator precedence, 													function argument enclosure,								indexing
[]      % array construction/concatenation,							empty matrix and array element deletion,		multiple output argument assignment
{}      % cell array assignment and contents
%         comment, 																			conversion specifier
%{ %}     block comments
!         operating system command
?       % metaclass for matlab class
''      % haracter array constructor
""      % string constructor
space   % separator
newline % separator
~       % logical not,																 	argument placeholder
=       % assignment
< &     % specify superclasses
.?      % specify fields of name-value structure


% string and char formatting
/ \     % file or folder path separation
..      % parent folder
*       % wildcard character
@       % class folder indicator
+       % package directory indicator

% uniq character sequences
''      % single quotation mark 
%%      % single percent sign
\\      % single backslash
\a      % alarm
\b      % backspace
\f      % form feed
\n      % new line
\r      % carriage return
\t      % horizontal tab
\v      % vertical tab
\xN     % hexadecimal number, n
\n      % octal number, n