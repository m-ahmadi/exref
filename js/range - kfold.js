N = a.length;
n_splits = 4;
n = Math.floor(N/n_splits);

// rng = [...Array(n_splits).keys()].slice(1)

[ 0, ...range(1,n_splits)].map(c => {
	let C = n * c;
	let [i0, i1] = [N-n-C, N-C];
	return {
		chunk: a.slice(i0,i1),
		rest: [ ...a.slice(0,i0), ...a.slice(i1) ]
	};
});

