/** Reference **/
// Objects can be passed as arguments to functions, and they can be returned by functions.
// Objects are passed by reference.
// Objects are not passed by value (objects are almost never copied).
// By Passing an object to a function it gives a reference or pointer to that function.
// The === operate compares object references, not values.
// "true" only if both operands are the same object:
// If you have 2 objects that contain the same properties and the same values,
// but are not the same object, then they will be found not to be equal.
//@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@@
/** C **/
// JavaScript is syntactically a C family language.
